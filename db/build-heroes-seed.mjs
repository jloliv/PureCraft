// Generates db/seed_heroes.sql — the 10 hand-curated hero recipes
// (bathroom-cleaner, glass-cleaner, etc.) extracted from constants/products.ts.
// These weren't in launch-recipes-v3.json, so they need their own seed pass.
//
// Run from project root:  node db/build-heroes-seed.mjs

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = dirname(here);

const sqlString = (v) => {
  if (v == null) return 'null';
  return `'${String(v).replace(/'/g, "''")}'`;
};
const sqlBool = (v) => (v ? 'true' : 'false');
const sqlJson = (obj) => `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`;

const GROUP_TO_KEY = {
  cleaning: 'cleaning',
  beauty: 'beauty-skincare',
  home: 'home-air-freshening',
};
const KEY_TO_LABEL = {
  cleaning: 'Cleaning',
  'beauty-skincare': 'Beauty & Skincare',
  'home-air-freshening': 'Home & Air Freshening',
};

const productsSrc = readFileSync(join(root, 'constants/products.ts'), 'utf8');

// Pull the PRODUCTS array — looks like:
//   { id: 'bathroom-cleaner', title: 'Bathroom Cleaner', group: 'cleaning', ... }
const productsBlock = productsSrc.slice(
  productsSrc.indexOf('export const PRODUCTS: Product[] = ['),
  productsSrc.indexOf('export const PRODUCT_GROUPS'),
);
const productLines = productsBlock
  .split('\n')
  .filter((l) => l.trim().startsWith('{ id:'));

const products = productLines.map((line) => {
  // Cheap parser — each product is one line of `{ id: '...', title: '...', ... }`
  // We just exec a regex per field.
  const f = (re) => {
    const m = line.match(re);
    return m ? m[1] : null;
  };
  const tagsMatch = line.match(/tags:\s*\[([^\]]*)\]/);
  const tags = tagsMatch
    ? [...tagsMatch[1].matchAll(/'([^']+)'/g)].map((m) => m[1])
    : [];
  return {
    id: f(/id:\s*'([^']+)'/),
    title: f(/title:\s*'([^']+)'/),
    group: f(/group:\s*'([^']+)'/),
    time: f(/time:\s*'([^']+)'/),
    savingsUsd: Number(f(/savingsUsd:\s*([\d.]+)/) ?? 0),
    storeBoughtUsd: Number(f(/storeBoughtUsd:\s*([\d.]+)/) ?? 0),
    tags,
    blurb: f(/blurb:\s*'([^']+)'/),
  };
});

// Pull the RECIPES dictionary — each entry is a multi-line object literal.
// We extract `productId`, `ingredients`, `steps`, `warnings`, `substitutions`.
function extractRecipes(src) {
  const startIdx = src.indexOf('export const RECIPES: Record<string, Recipe> = {');
  const endIdx = src.indexOf('\nexport function findRecipe', startIdx);
  const body = src.slice(startIdx, endIdx);
  // Match each top-level "  'id':" or "  id:" key.
  const keyRe = /^ {2}(?:'([^']+)'|([a-zA-Z][\w-]*)):\s*\{/gm;
  const out = {};
  let m;
  while ((m = keyRe.exec(body)) !== null) {
    const key = m[1] ?? m[2];
    let depth = 1;
    let i = m.index + m[0].length;
    while (i < body.length && depth > 0) {
      const ch = body[i];
      if (ch === '{') depth++;
      else if (ch === '}') depth--;
      i++;
    }
    out[key] = body.slice(m.index + m[0].length, i - 1);
  }
  return out;
}
const RECIPES = extractRecipes(productsSrc);

function extractStringArray(body, fieldName) {
  // Match `fieldName: [ ... ]` — handles multi-line arrays of single-quoted strings.
  const re = new RegExp(fieldName + ":\\s*\\[([\\s\\S]*?)\\]", 'm');
  const m = body.match(re);
  if (!m) return [];
  const items = [];
  const itemRe = /'((?:[^'\\]|\\.)*)'/g;
  let im;
  while ((im = itemRe.exec(m[1])) !== null) {
    items.push(im[1].replace(/\\'/g, "'").replace(/\\n/g, '\n'));
  }
  return items;
}

function extractIngredients(body) {
  // Each ingredient is: { name: '...', amount: '...', haveIt?: bool, storePriceUsd?: number }
  const m = body.match(/ingredients:\s*\[([\s\S]*?)\],/m);
  if (!m) return [];
  const inner = m[1];
  const out = [];
  // Walk each `{ ... }` chunk.
  const objRe = /\{([^}]+)\}/g;
  let om;
  while ((om = objRe.exec(inner)) !== null) {
    const obj = om[1];
    const name = (obj.match(/name:\s*'([^']+)'/) ?? [])[1];
    const amount = (obj.match(/amount:\s*'([^']+)'/) ?? [])[1];
    if (!name || !amount) continue;
    out.push(`${amount} ${name}`);
  }
  return out;
}

function inferTags(p, key) {
  const t = new Set(p.tags);
  const safe = p.tags.some((t) => /baby|family|safe/i.test(t));
  if (safe) t.add('family-safe');
  const haystack = p.title.toLowerCase() + ' ' + (p.blurb ?? '').toLowerCase();
  if (/spray|mist/.test(haystack)) t.add('spray');
  if (/scrub/.test(haystack)) t.add('scrub');
  if (/oil/.test(haystack)) t.add('oil');
  if (key === 'home-air-freshening') t.add('spray');
  return Array.from(t);
}

const out = [];
out.push('-- AUTO-GENERATED by db/build-heroes-seed.mjs');
out.push('-- The 10 hand-curated hero recipes from constants/products.ts.');
out.push('begin;');
out.push('');

for (const p of products) {
  const recipeBody = RECIPES[p.id] ?? '';
  const ingredients = extractIngredients(recipeBody);
  const steps = extractStringArray(recipeBody, 'steps');
  const key = GROUP_TO_KEY[p.group] ?? 'cleaning';
  const label = KEY_TO_LABEL[key];
  const tags = inferTags(p, key);
  const safeForKids = p.tags.some((t) => /baby|family/i.test(t));

  out.push(
    `insert into public.recipes (id, title, category_label, category_key, difficulty, time_label, ingredients, instructions, safe_for_kids, cost_savings, tags, source, is_published) values (${[
      sqlString(p.id),
      sqlString(p.title),
      sqlString(label),
      sqlString(key),
      sqlString('Easy'),
      sqlString(p.time),
      sqlJson(ingredients),
      sqlJson(steps),
      sqlBool(safeForKids),
      sqlString(`$${p.savingsUsd.toFixed(2)} saved`),
      sqlJson(tags),
      sqlString('catalog'),
      'true',
    ].join(', ')}) on conflict (id) do update set
  title = excluded.title,
  category_label = excluded.category_label,
  category_key = excluded.category_key,
  difficulty = excluded.difficulty,
  time_label = excluded.time_label,
  ingredients = excluded.ingredients,
  instructions = excluded.instructions,
  safe_for_kids = excluded.safe_for_kids,
  cost_savings = excluded.cost_savings,
  tags = excluded.tags;`,
  );
}

out.push('');
out.push('commit;');

writeFileSync(join(here, 'seed_heroes.sql'), out.join('\n') + '\n');
console.log('✓ wrote db/seed_heroes.sql');
console.log('  hero recipes: ' + products.length);
for (const p of products) {
  const recipeBody = RECIPES[p.id] ?? '';
  const ings = extractIngredients(recipeBody).length;
  const steps = extractStringArray(recipeBody, 'steps').length;
  console.log(`    ${p.id.padEnd(20)} ings=${ings} steps=${steps}`);
}
